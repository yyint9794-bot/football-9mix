import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const gradlePath = join(root, 'android', 'app', 'build.gradle');
const gradle = readFileSync(gradlePath, 'utf8');

const versionCode = Number(/versionCode\s+(\d+)/.exec(gradle)?.[1] ?? 1);
const versionName = /versionName\s+"([^"]+)"/.exec(gradle)?.[1] ?? '1.0';

const payload = {
  versionCode,
  versionName,
  apkUrl: `https://ballpwal.org/downloads/9mix-football.apk?v=${versionCode}`,
  releaseNotes: 'ဒေတာ၊ တိုက်ရိုက်ကြည့်၊ Update စစ်ဆေးမှု ပြင်ဆင်ချက်',
};

const outPath = join(root, 'public', 'app-version.json');
writeFileSync(outPath, JSON.stringify(payload, null, 2), 'utf8');
console.log(`Wrote ${outPath} — v${versionName} (${versionCode})`);

const generatedDir = join(root, 'functions', '_generated');
mkdirSync(generatedDir, { recursive: true });
const generatedPath = join(generatedDir, 'appVersion.mjs');
const generatedSource = `/** Auto-generated by scripts/write-app-version.mjs — do not edit */
export const APP_VERSION = ${JSON.stringify(payload, null, 2)};
`;
writeFileSync(generatedPath, generatedSource, 'utf8');
console.log(`Wrote ${generatedPath}`);

const appDownloadPath = join(root, 'src', 'appDownload.ts');
const appDownload = readFileSync(appDownloadPath, 'utf8');
const nextBuild = `v${versionCode}`;
const updated = appDownload.replace(
  /export const APP_APK_BUILD = '[^']*';/,
  `export const APP_APK_BUILD = '${nextBuild}';`,
);
if (updated === appDownload) {
  console.warn('appDownload.ts: APP_APK_BUILD line not updated');
} else {
  writeFileSync(appDownloadPath, updated, 'utf8');
  console.log(`Updated ${appDownloadPath} — APP_APK_BUILD=${nextBuild}`);
}
