import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const gradlePath = join(root, 'android', 'app', 'build.gradle');
const gradle = readFileSync(gradlePath, 'utf8');

const versionCode = Number(/versionCode\s+(\d+)/.exec(gradle)?.[1] ?? 1);
const versionName = /versionName\s+"([^"]+)"/.exec(gradle)?.[1] ?? '1.0';

const jsDelivrApk = `https://cdn.jsdelivr.net/gh/yyint9794-bot/football-9mix@main/public/downloads/9mix-football.apk`;
const siteApk = `https://ballpwal.org/downloads/9mix-football.apk?v=${versionCode}`;

const payload = {
  versionCode,
  versionName,
  apkUrl: jsDelivrApk,
  apkUrlCdn: jsDelivrApk,
  apkUrlSite: siteApk,
  releaseNotes: 'ဒေတာ၊ တိုက်ရိုက်ကြည့်၊ Update စစ်ဆေးမှု ပြင်ဆင်ချက်',
};

const outPath = join(root, 'public', 'app-version.json');
writeFileSync(outPath, JSON.stringify(payload, null, 2), 'utf8');
console.log(`Wrote ${outPath} — v${versionName} (${versionCode})`);

const generatedDir = join(root, 'functions', '_generated');
mkdirSync(generatedDir, { recursive: true });
writeFileSync(
  join(generatedDir, 'appVersion.mjs'),
  `/** Auto-generated by scripts/write-app-version.mjs — do not edit */
export const APP_VERSION = ${JSON.stringify(payload, null, 2)};
`,
  'utf8',
);

const publishedPath = join(root, 'src', 'native', 'publishedVersion.ts');
writeFileSync(
  publishedPath,
  `/** Auto-generated — APK ထဲတွင် ဗားရှင်း အနည်းဆုံး (build.gradle) */
export const PUBLISHED_VERSION_CODE = ${versionCode};
export const PUBLISHED_VERSION_NAME = '${versionName}';
export const PUBLISHED_APK_URL = '${jsDelivrApk}';
export const PUBLISHED_APK_URL_SITE = '${siteApk}';
export const PUBLISHED_RELEASE_NOTES = ${JSON.stringify(payload.releaseNotes)};
`,
  'utf8',
);
console.log(`Wrote ${publishedPath}`);

const appDownloadPath = join(root, 'src', 'appDownload.ts');
const appDownload = readFileSync(appDownloadPath, 'utf8');
const nextBuild = `v${versionCode}`;
const updated = appDownload.replace(
  /export const APP_APK_BUILD = '[^']*';/,
  `export const APP_APK_BUILD = '${nextBuild}';`,
).replace(
  /export const APP_APK_CDN_URL =\s*\n?\s*'[^']*';/,
  `export const APP_APK_CDN_URL =\n  '${jsDelivrApk}';`,
);
if (updated === appDownload) {
  console.warn('appDownload.ts: APP_APK_BUILD line not updated');
} else {
  writeFileSync(appDownloadPath, updated, 'utf8');
  console.log(`Updated ${appDownloadPath} — APP_APK_BUILD=${nextBuild}`);
}
